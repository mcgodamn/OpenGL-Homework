void circle(float myx,float myy,float half)
{
	float yy;
	float xx;
	glColor3f(1, 0.0, 0.0);
	glBegin(GL_POLYGON);
	for (float i = -1*half; i <= half; ++i)
	{
		yy = pow(half,2) - pow(i,2);
		yy = sqrt(yy);
		yy *= -1;
		yy += myy;
		xx = i + myx;
		glVertex3f(x(xx), y(yy), 0.2);
	}

	for (float i = half; i >= -1 * half; --i)
	{
		yy = pow(half, 2) - pow(i, 2);
		yy = sqrt(yy);
		yy += myy;
		xx = i + myx;
		glVertex3f(x(xx), y(yy), 0.2);
	}
	glEnd();
}
